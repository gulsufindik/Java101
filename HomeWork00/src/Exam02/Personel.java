package Exam02;

public class Personel {
	private String ad;
    private int yaş;

    public Personel(String ad, int yaş) {
        this.ad = ad;
        this.yaş = yaş;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public int getYaş() {
        return yaş;
    }

    public void setYaş(int yaş) {
        this.yaş = yaş;
    }
}


