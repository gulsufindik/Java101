package Exam01;

public class Departman {
	private String ad;
	private String mudur;

	public Departman(String ad, String mudur) {
		this.ad = ad;
		this.mudur = mudur;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public String getMudur() {
		return mudur;
	}

	public void setMudur(String mudur) {
		this.mudur = mudur;
	}

}
